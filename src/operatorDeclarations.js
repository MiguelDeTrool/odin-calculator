function sum(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    let result = a + b;
    return result;
}

function subtract(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    let result = a - b;
    return result;
}

function multiply(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    let result = a * b;
    return result;
}

function divide(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    let result = a / b;
    return result;
}